package com.pontusvision.gdpr;

public class PVGridColumn {

    Boolean defaultSortAsc;

    String field;

    Boolean focusable;

    String headerCssClass;
    String id;

    Integer minWidth;

    String name;

    Boolean rerenderOnResize;

    Boolean resizable;
    Boolean selectable;

    Boolean sortable;
    Integer width;

    public Boolean getDefaultSortAsc() {
        return defaultSortAsc;
    }

    public void setDefaultSortAsc(Boolean defaultSortAsc) {
        this.defaultSortAsc = defaultSortAsc;
    }

    public String getField() {
        return field;
    }

    public void setField(String field) {
        this.field = field;
    }

    public Boolean getFocusable() {
        return focusable;
    }

    public void setFocusable(Boolean focusable) {
        this.focusable = focusable;
    }

    public String getHeaderCssClass() {
        return headerCssClass;
    }

    public void setHeaderCssClass(String headerCssClass) {
        this.headerCssClass = headerCssClass;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Integer getMinWidth() {
        return minWidth;
    }

    public void setMinWidth(Integer minWidth) {
        this.minWidth = minWidth;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Boolean getRerenderOnResize() {
        return rerenderOnResize;
    }

    public void setRerenderOnResize(Boolean rerenderOnResize) {
        this.rerenderOnResize = rerenderOnResize;
    }

    public Boolean getResizable() {
        return resizable;
    }

    public void setResizable(Boolean resizable) {
        this.resizable = resizable;
    }

    public Boolean getSelectable() {
        return selectable;
    }

    public void setSelectable(Boolean selectable) {
        this.selectable = selectable;
    }

    public Boolean getSortable() {
        return sortable;
    }

    public void setSortable(Boolean sortable) {
        this.sortable = sortable;
    }

    public Integer getWidth() {
        return width;
    }

    public void setWidth(Integer width) {
        this.width = width;
    }
}
